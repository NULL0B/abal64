#ifndef	_palet_h
#define	_palet_h

/*	Twin Server 2.4	: Standard Palette definitions		*/
/*	----------------------------------------------		*/

/*	Of the 256 colours in the palette the first 16		*/
/*	are reserved for the standard CICO colours.		*/
/*	The range 0x0010 to 0x001F are reserved for use		*/
/*	by GUI design tools for the construction and		*/
/*	painting of widgets.					*/
	

#define	_DEFAULT_GUI_BLACK		0x0010
#define	_DEFAULT_GUI_DARK_LOW		0x0011
#define	_DEFAULT_GUI_DARK		0x0012
#define	_DEFAULT_GUI_GREYED		0x0013
#define	_DEFAULT_GUI_NEUTRAL_LOW	0x0014
#define	_DEFAULT_GUI_NEUTRAL		0x0015
#define	_DEFAULT_GUI_LIGHT		0x0016
#define	_DEFAULT_GUI_LIGHT_HIGH		0x0017
#define	_DEFAULT_GUI_WHITE    		0x0018
#define	_DEFAULT_GUI_FG_TITLE 		0x0019
#define	_DEFAULT_GUI_BG_TITLE 		0x001A
#define	_DEFAULT_GUI_FG_BUBBLE		0x001B
#define	_DEFAULT_GUI_BG_BUBBLE		0x001C
#define	_DEFAULT_GUI_FOCUS   		0x001D
#define	_DEFAULT_GUI_RES1     		0x001E
#define	_DEFAULT_GUI_RES2    		0x001F

#endif	/* _palet_h */

